    @Override
    public View onCreateView(@NonNull LayoutInflater inflater, @Nullable ViewGroup container, @Nullable Bundle savedInstanceState) {
        mView = inflater.inflate(R.layout.fragment_history, container, false);
        return mView;
    }

    public void tempChart(HourlyHistory value) {
        LineChart chart = mView.findViewById(R.id.tempChart);
        List<Entry> tempHigh = new ArrayList<>();
        List<Entry> tempLow = new ArrayList<>();
        List<Entry> tempAverage = new ArrayList<>();
        List<Entry> hiddenTopRow = new ArrayList<>();
        int max = 0;
        for (HourlyObservation observations : value.observations) {
            tempHigh.add(new Entry(observations.epoch, observations.metric.tempHigh));
            tempLow.add(new Entry(observations.epoch, observations.metric.tempLow));
            tempAverage.add(new Entry(observations.epoch, observations.metric.tempAvg));

            if (observations.metric.tempHigh > max)
                max = observations.metric.tempHigh;
        }

        for (Entry entry : tempHigh) {
            hiddenTopRow.add(new Entry(entry.getX(), max + 2.5f));
        }

        Calendar c = Calendar.getInstance(TimeZone.getTimeZone("UTC"));
        c.add(Calendar.DAY_OF_YEAR, -1);

        long difference = (Calendar.getInstance(TimeZone.getTimeZone("UTC")).getTimeInMillis() / 1000L) - (c.getTimeInMillis() / 1000L);

        chart.getXAxis().setDrawGridLines(false);
        chart.getAxisLeft().setGranularity(5f);
        chart.getAxisRight().setEnabled(false);
        chart.setHighlightPerDragEnabled(true);

        chart.setOnChartValueSelectedListener(new OnChartValueSelectedListener() {
            @Override
            public void onValueSelected(Entry e, Highlight h) {

                Highlight highlight[] = new Highlight[chart.getData().getDataSets().size()];
                for (int j = 0; j < chart.getData().getDataSets().size(); j++) {

                    IDataSet iDataSet = chart.getData().getDataSets().get(j);

                    for (int i = 0; i < ((LineDataSet) iDataSet).getValues().size(); i++) {
                        if (((LineDataSet) iDataSet).getValues().get(i).getX() == e.getX()) {
                            highlight[j] = new Highlight(e.getX(), e.getY(), j);
                        }
                    }

                }
                chart.highlightValues(highlight);
            }

            @Override
            public void onNothingSelected() {
            }
        });

        LineDataSet tempHighDataSet = new LineDataSet(tempHigh, "High");
        tempHighDataSet.setColor(getResources().getColor(R.color.red));
        tempHighDataSet.setCircleColorHole(getResources().getColor(R.color.red));
        tempHighDataSet.setCircleColor(getResources().getColor(R.color.white));
        tempHighDataSet.setCircleHoleRadius(2.5f);
        tempHighDataSet.setDrawValues(false);
        tempHighDataSet.setHighLightColor(getResources().getColor(android.R.color.black));
        tempHighDataSet.setDrawHorizontalHighlightIndicator(false);

        LineDataSet tempLowDataSet = new LineDataSet(tempLow, "Low");
        tempLowDataSet.setColor(getResources().getColor(R.color.blue));
        tempLowDataSet.setCircleColorHole(getResources().getColor(R.color.blue));
        tempLowDataSet.setCircleColor(getResources().getColor(R.color.white));
        tempLowDataSet.setCircleHoleRadius(2.5f);
        tempLowDataSet.setDrawValues(false);
        tempLowDataSet.setHighLightColor(getResources().getColor(android.R.color.black));
        tempLowDataSet.setDrawHorizontalHighlightIndicator(false);

        LineDataSet tempAverageDataSet = new LineDataSet(tempAverage, "Average");
        tempAverageDataSet.setColor(getResources().getColor(R.color.orange));
        tempAverageDataSet.setCircleColorHole(getResources().getColor(R.color.orange));
        tempAverageDataSet.setCircleColor(getResources().getColor(R.color.white));
        tempAverageDataSet.setCircleHoleRadius(2.5f);
        tempAverageDataSet.setDrawValues(false);
        tempAverageDataSet.setHighLightColor(getResources().getColor(android.R.color.black));
        tempAverageDataSet.setDrawHorizontalHighlightIndicator(false);

        LineDataSet hiddenDataSet = new LineDataSet(hiddenTopRow, "");
        hiddenDataSet.setColor(getResources().getColor(android.R.color.transparent));
        hiddenDataSet.setCircleColorHole(getResources().getColor(android.R.color.transparent));
        hiddenDataSet.setCircleColor(getResources().getColor(android.R.color.transparent));
        hiddenDataSet.setDrawValues(false);
        hiddenDataSet.setHighLightColor(getResources().getColor(android.R.color.black));
        hiddenDataSet.setDrawHorizontalHighlightIndicator(false);

        LineData lineData = new LineData(tempHighDataSet, tempLowDataSet, tempAverageDataSet, hiddenDataSet);
        chart.setData(lineData);
        chart.setDragEnabled(true);

        chart.getXAxis().setPosition(XAxis.XAxisPosition.BOTTOM);
        Description description = new Description();
        description.setText("°C");
        chart.setDescription(description);

        chart.getXAxis().setValueFormatter(new DateValueFormatter(new SimpleDateFormat("EE ha")));

        chart.setVisibleXRangeMaximum(difference);
        chart.setVisibleXRangeMinimum(difference);
        chart.moveViewToX(tempAverage.get(tempAverage.size() - 1).getX());

        SimpleDateFormat format = new SimpleDateFormat("h:mma dd/MM");

        chart.setMarker(new CustomChartMarker(getContext(), R.layout.marker, true, "°C", format, getResources().getColor(R.color.red), getResources().getColor(R.color.blue), getResources().getColor(R.color.orange), getResources().getColor(android.R.color.black)));
        chart.setScaleEnabled(false);

        getActivity().runOnUiThread(new Runnable() {
            @Override
            public void run() {
                chart.animateXY(500, 500);
            }
        });
    }

