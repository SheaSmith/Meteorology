    private View mView;

    @Override
    public void onActivityCreated(@Nullable Bundle savedInstanceState) {
        super.onActivityCreated(savedInstanceState);

        ApiManager.getHourlyHistory(new ApiResponse<HourlyHistory>() {
            @Override
            public void success(HourlyHistory value) {
                tempChart(value);
                rain(value);
                wind(value);
                pressure(value);
                humidity(value);
                otherTemp(value);
                solarRadiation(value);
                uv(value);
            }

            @Override
            public void error(Exception e) {
                e.printStackTrace();
            }
        }, getContext());
    }

